export default class Calculator {
  static add(value1, value2) {
    return value1 + value2;
  }

  static subtract(value1, value2) {
    return value1 - value2;
  }

  static multiply(value1, value2) {
    return value1 * value2;
  }

  static divide(value1, value2) {
    return value1 / value2;
  }
}
